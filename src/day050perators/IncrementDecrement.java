package day050perators;

public class IncrementDecrement {

	public static void main(String[] args) {
		// int variable 
		//carpma ve b√∂lme ile artirin ve azaltin
		
		int num1 = 12;//uzun yol ile
		
		num1 = num1 * 3;//36
		System.out.println(num1);
		
		//kisa yol ile
		num1*=3;//36+3=108
		System.out.println(num1);
		
		//uzun yol ile bolme decrement
		num1 = num1 / 9;//108/9=12
		System.out.println(num1);//12
		
		//kisa yol
		num1/=2;
		System.out.println(num1);//12/2=6
		
		

	}

}
