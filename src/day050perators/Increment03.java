package day050perators;

public class Increment03 {

	public static void main(String[] args) {
		// int data tipiinde bir varriable olusturun 
		//bunlari en kisa yolu kullanarak bir  = 1 artirin
		// en kisa yol sadece bir artirmada kullanilir
		
		int sayi1 = 12;
		sayi1++;// vRIbleden sonra ++ iki kere yazinca degeri sadece bir artirir
		
		System.out.println(sayi1);
		
		int num1 = 20;
		num1 = num1 + 1;//21
		num1+=1;//22
		num1++;//23
		System.out.println(num1);//23
		
		

	}

}
